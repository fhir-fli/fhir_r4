      case 'procedure': return ObservationCategoryCodes.procedure;
      case 'survey': return ObservationCategoryCodes.survey;
      case 'exam': return ObservationCategoryCodes.exam;
      case 'therapy': return ObservationCategoryCodes.therapy;
      case 'activity': return ObservationCategoryCodes.activity;
